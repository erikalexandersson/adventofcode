package se.erikalexandersson.adventofcode.helpers;

public class Acceleration {

	private int ax;
	private int ay;
	private int az;

	public Acceleration(int ax, int ay, int az) {
		this.ax = ax;
		this.ay = ay;
		this.az = az;
	}

	public int getAx() {
		return ax;
	}

	public int getAy() {
		return ay;
	}

	public int getAz() {
		return az;
	}

}
